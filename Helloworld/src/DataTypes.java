/**DATATYPES
 * Primitive Datatypes and reference datatypes
 * Primitive datatype = Byte, int, short, float, double, boolean
 * whatever declared inside class is instance variable and in class
 * object is an instance of class
 * it will be a instance of a class
 * final will not change an constant value
 **/


public class DataTypes {
    public static void main(String[] args) {
        byte age = 30;
        short number = 10000;
        int number1 = 19999200;
        long number3 = 22080980000000L;
        final float pi = 3.14f;
        double decimalValue = 343.000;
        char hello = 'A';

        System.out.println(age);
        System.out.println(number);
        System.out.println(number1);
        System.out.println(number3);
        System.out.println(pi);
        System.out.println(decimalValue);
        System.out.println(hello);

        //Refence Types
        String name = "Nancy";
        Integer num = 50;

    }
}
